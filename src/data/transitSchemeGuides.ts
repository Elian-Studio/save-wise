// calcExample은 KPASS/CLIMATE 상수와 kpassRefund로 계산 생성 — 상수가 바뀌면 예시도 따라가 드리프트 방지.
import type { SchemeId } from './transitSchemes';
import { KPASS, CLIMATE, type AgeTier } from './transitCards';
import { kpassRefund } from '../lib/transitSchemeRec';

export interface SchemeGuide {
  eligibility: { heading: string; paras: string[] };
  applyDetail: { step: string; detail: string }[];
  calcExample: { title: string; rows: { label: string; value: string }[]; note: string };
  cautions: string[];
  faq: { q: string; a: string }[];
}

const won = (n: number): string => `${n.toLocaleString('ko-KR')}원`;

// K-패스 계열(kpass/gyeonggi/incheon) 계산 예시를 상수에서 생성. 마지막 행 = 실제 환급액(유리한 쪽).
function kpassExample(opts: {
  title: string;
  spend: number;
  tier: AgeTier;
  wide: boolean;
  tierLabel: string;
  tripLabel: string;
  note: string;
}): SchemeGuide['calcExample'] {
  const { refund, threshold } = kpassRefund(opts.spend, opts.tier, opts.wide);
  const rate = KPASS.rate[opts.tier];
  const byRate = Math.round(opts.spend * rate);
  return {
    title: opts.title,
    rows: [
      { label: `월 교통비 (${opts.tripLabel})`, value: won(opts.spend) },
      { label: `비율 환급 (${opts.tierLabel} ${Math.round(rate * 100)}%)`, value: won(byRate) },
      {
        label: `기준금액 초과분 (${won(opts.spend)} − ${won(threshold)})`,
        value: won(opts.spend - threshold),
      },
      { label: '이번 달 환급액 (유리한 쪽 자동)', value: won(refund) },
    ],
    note: opts.note,
  };
}

